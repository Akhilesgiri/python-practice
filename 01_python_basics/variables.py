# What is a Variable?
# A variable is used to store data in memory.
# We can store:
# - text
# - numbers
# - decimal values
# - true/false values
#
# Think of a variable like a container
# that stores some value.


# String means text data.
# Strings are written inside quotes.
name = "Akhil"
print(name)


# Integer means whole numbers.
age = 22
print(age)


# Float means decimal numbers.
height = 5.8
print(height)


# Boolean stores only:
# True or False
is_python_developer = True
print(is_python_developer)



city = "Hyderabad"
salary = 45000

print(city)
print(salary)


print("City:", city)
print("Salary:", salary)


#type
print(type(name))       # string
print(type(age))        # integer
print(type(height))     # float
print(type(is_python_developer))  # boolean



x = 10
print(x)

# Reassigning new value
x = 20
print(x)



first_name = "Akhil"
last_name = "Esgiri"

print(first_name)
print(last_name)


